def create_generator(image_paths, ages, genders, datagen, batch_size=32, shuffle=True):
    """Create a generator that yields batches of data"""

    while True:
        if shuffle:
            indices = np.random.permutation(len(image_paths))
        else:
            indices = np.arange(len(image_paths))

        for start_idx in range(0, len(image_paths), batch_size):
            end_idx = min(start_idx + batch_size, len(image_paths))
            batch_indices = indices[start_idx:end_idx]

            batch_images = []
            batch_ages = []
            batch_genders = []

            for idx in batch_indices:
                img = load_and_preprocess_image(image_paths[idx])
                batch_images.append(img)
                batch_ages.append(ages[idx])
                batch_genders.append(genders[idx])

            batch_images = np.array(batch_images)
            batch_ages = np.array(batch_ages).reshape(-1, 1)
            batch_genders = np.array(batch_genders).reshape(-1, 1)

            # Apply augmentation to training data
            if datagen is not None:
                batch_images = datagen.random_transform(batch_images[0])
                batch_images = batch_images.reshape(1, IMAGE_SIZE, IMAGE_SIZE, 3)
                # Apply same transformation to rest (simplified)

            yield ([batch_images, batch_images], [batch_ages, batch_genders])

