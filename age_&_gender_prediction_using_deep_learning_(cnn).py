def predict_age_gender(image_path, model, input_size=IMAGE_SIZE):
    """Predict age and gender for a single image"""
    img = load_and_preprocess_image(image_path, (input_size, input_size))
    img = img.reshape(1, input_size, input_size, 3)

    age_pred, gender_pred = model.predict(img, verbose=0)

    gender_label = "Male" if gender_pred[0][0] > 0.5 else "Female"
    age_value = age_pred[0][0]

    return age_value, gender_label

